const x = Math.floor(Math.random() * 100) + 1; // non-uniform
const y = Math.floor(Math.random() * 50) + 1;
console.log(`${x} + ${y} = ${x + y}`);
console.log(`${x} - ${y} = ${x - y}`);
console.log(`${x} * ${y} = ${x * y}`);
console.log(`${x} / ${y} = ${x / y}`);
console.log(`${x} % ${y} = ${x % y}`);